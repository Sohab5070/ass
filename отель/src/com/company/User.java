package com.company;

public class User
{
    private String name;
    private String surname;
    private int age;
    private String city;

    public String getName() {
        return this.name;
    }
    public void setName(String name) {
        this.name = name;
    }

    public String Surname() {
        return this.surname;
    }
    public void setSurname(String surname) {
        this.surname = surname;
    }

    public int Age() {
        return this.age;
    }
    public void setAge(int age) {
        this.age = age;
    }

    public String City() {
        return this.city;
    }
    public void setCity(String city) {
        this.city = city;
    }
}
